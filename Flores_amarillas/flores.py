from turtle import *
import turtle as tur

tur.setup(width=900, height=700)


tur1 = tur.Turtle()
tur1.speed(11)
tur2 = tur.Turtle()
def dibujar_flor_1(tur):
    tur.penup()
    tur.left(90)
    tur.forward(200)
    tur.pendown()
    tur.right(90)

    tur.fillcolor("red")
    tur.begin_fill()
    tur.circle(10, 180)
    tur.circle(25, 110)
    tur.left(50)
    tur.circle(60, 45)
    tur.circle(20, 170)
    tur.right(24)
    tur.forward(30)
    tur.left(10)
    tur.circle(30, 110)
    tur.forward(20)
    tur.left(40)
    tur.circle(90, 70)
    tur.circle(30, 150)
    tur.right(30)
    tur.forward(15)
    tur.circle(80, 90)
    tur.left(15)
    tur.forward(45)
    tur.right(165)
    tur.forward(20)
    tur.left(155)
    tur.circle(150, 80)
    tur.left(50)
    tur.circle(150, 90)
    tur.end_fill()

    tur.left(150)
    tur.circle(-90, 70)
    tur.left(20)
    tur.circle(75, 105)
    tur.setheading(60)
    tur.circle(80, 98)
    tur.circle(-90, 40)

    tur.left(180)
    tur.circle(90, 40)
    tur.circle(-80, 98)
    tur.setheading(-83)

    tur.forward(30)
    tur.left(90)
    tur.forward(25)
    tur.left(45)
    tur.fillcolor("yellow green")
    tur.begin_fill()
    tur.circle(-80, 90)
    tur.right(90)
    tur.circle(-80, 90)
    tur.end_fill()
    tur.right(135)
    tur.forward(60)
    tur.left(180)
    tur.forward(85)
    tur.left(90)
    tur.forward(80)

    tur.right(90)
    tur.settiltangle(45)
    tur.fillcolor("yellow green")
    tur.begin_fill()
    tur.circle(80, 90)
    tur.left(90)
    tur.circle(80, 90)
    tur.end_fill()
    tur.left(135)
    tur.forward(60)
    tur.left(180)
    tur.forward(60)
    tur.right(150)
    tur.circle(200, 60)

